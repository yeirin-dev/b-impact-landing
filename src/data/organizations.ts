// 지역별 그룹핑 유틸리티
export function getRegionGroups() {
  const groups: Record<string, Organization[]> = {};
  organizations.forEach((org) => {
    const region = org.region.replace("부산광역시 ", "");
    if (!groups[region]) groups[region] = [];
    groups[region].push(org);
  });
  return groups;
}

export function getUniqueRegions() {
  return [
    ...new Set(
      organizations.map((o) => o.region.replace("부산광역시 ", ""))
    ),
  ].sort();
}
